//phan biet su khac nhau giua i++ va ++i
#include <stdio.h>
int main()
{
	int b=10;
	int a=b++;
	int c=++b;
	printf ("b=10");
	printf ("\nVoi a=b++, ta thay a=%d Chung to phep toan tren gan a=b=10 truoc roi moi tang b them 1 don vi=11", a);
	printf ("\nVoi c=++b, ta thay c=%d Chung to phep toan tren tang b them 1 don vi(da tang them 1 don vi tu truoc do=11) =12 roi moi gan c=b=11 ", c);
	printf ("\nPhep toan ++ thay doi gia tri cua ca 2 bien sau khi thuc hien");
}


